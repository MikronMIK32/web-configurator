import { useState } from 'react';
import { useStore } from 'react-redux';

import { useGenerateProject } from '@api/generator';

import Button from '@components/controls/Button';
import Loader from '@components/controls/Loader';

import { colors } from '@scripts/colors';
import { scale } from '@scripts/helpers';
import { RootState } from '@store/index';

const ExportView = () => {
  const generateProject = useGenerateProject();
  const store = useStore<RootState>();

  // eslint-disable-next-line prefer-destructuring
  const isLoading = generateProject.isPending;

  const [data, setData] = useState<{
    error?: string;
    link?: string;
    isLoaded: boolean;
  }>({
    isLoaded: false,
  });

  return (
    <div css={{ paddingTop: scale(2) }}>
      {isLoading && <Loader message="Отправка запроса..." />}
      <p
        css={{
          background: colors.grey100,
          padding: scale(2),
          borderRadius: scale(1),
          maxWidth: scale(120),
          marginBottom: scale(2),
        }}
      >
        В ближайшее время здесь появится сводка по проекту. Будут учтены заполненность периферий, наличие пересечений и
        ошибок в данных. Пока что вслепую позволяем передать запрос на API генератора кода.
      </p>
      {data.isLoaded && (
        <div
          css={{
            maxWidth: scale(120),
            marginBottom: scale(2),
            padding: scale(2),
            borderRadius: scale(1),
            ...(data.error
              ? {
                  background: colors.error,
                  color: colors.white,
                }
              : {
                  background: colors.success,
                  color: colors.black,
                }),
          }}
        >
          {data.error ? (
            data.error
          ) : (
            <p>
              Ваша ссылка на скачивание:
              <Button as="a" href={data.link} download>
                скачать
              </Button>
            </p>
          )}
        </div>
      )}
      <Button
        disabled={data.isLoaded}
        onClick={async () => {
          try {
            const result = await generateProject.mutateAsync({
              project: store.getState().project,
            });

            setData({
              isLoaded: true,
              link: result.data.url,
            });
          } catch (err: any) {
            console.error(err);
            setData({
              isLoaded: true,
              error: err.message,
            });
          }
        }}
      >
        Сгенерировать архив проекта
      </Button>
    </div>
  );
};

export default ExportView;
